// xorAndPermute XORs rate (136) bytes of buf into state, then permutes.
// Uses AVX2 VPXOR for the XOR phase, then tail-calls keccakF1600.
//
// func xorAndPermute(state *[200]byte, buf *byte)
TEXT ·xorAndPermute(SB), NOSPLIT, $0-16
	MOVQ state+0(FP), DI
	MOVQ buf+8(FP), SI

	// XOR 136 bytes: 4 × 32B (AVX2) + 1 × 8B
	VMOVDQU 0(SI), Y0
	VPXOR   0(DI), Y0, Y0
	VMOVDQU Y0, 0(DI)

	VMOVDQU 32(SI), Y0
	VPXOR   32(DI), Y0, Y0
	VMOVDQU Y0, 32(DI)

	VMOVDQU 64(SI), Y0
	VPXOR   64(DI), Y0, Y0
	VMOVDQU Y0, 64(DI)

	VMOVDQU 96(SI), Y0
	VPXOR   96(DI), Y0, Y0
	VMOVDQU Y0, 96(DI)

	MOVQ  128(SI), AX
	XORQ  128(DI), AX
	MOVQ  AX, 128(DI)

	VZEROUPPER

	// Tail-call permutation. state+0(FP) == a+0(FP) (first arg is same).
	JMP ·keccakF1600(SB)

